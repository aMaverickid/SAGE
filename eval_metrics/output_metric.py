"""Output Metric: does the description predict what the feature *causes*?

Pipeline (ported from ``feature_descriptions_pipeline.ipynb``):
    1. For 4 KL-divergence targets [0.25, 0.5, -0.25, -0.5], binary-search
       the steering strength for the candidate feature and generate
       completions on 3 fixed prompts.
    2. Load 2 pre-generated random-feature completion sets from the cached
       random-amps pool (see ``scripts/prepare_random_amps.py``).
    3. Present all 3 sets to the judge LLM in random order, give it the
       description, and ask which set the description matches.
    4. Success = LLM picks the candidate-feature set.

The notebook's KL binary search needs a local HookedSAETransformer + SAE;
there is no API equivalent. ``backend="local"`` is therefore the only
fully faithful path. ``backend="api"`` is provided as a degraded fallback
that uses fixed strengths via ``/api/steer`` for users without GPUs —
the result schema is identical so the CLI can produce comparable rows.
"""
from __future__ import annotations

import pickle
import random
import re
from dataclasses import asdict, dataclass, field
from pathlib import Path
from typing import Any, Dict, List, Optional, Sequence, Tuple

from core.agent import ask_agent
from eval_metrics.local_backend import (
    GEN_PROMPTS_DEFAULT,
    KL_DIV_VALUES_DEFAULT,
    get_completions_for_kl_val,
)

NUM_OF_DISTS = 2  # how many random-feature distractor sets are shown to the judge

JUDGE_SYS_PROMPT = (
    "You are analyzing a specific neuron in a language model. You will "
    "receive: (1) a LABEL describing what the neuron detects; (2) three "
    "sets of model completions — two from random neurons and one from "
    "the labelled neuron, generated by amplifying activation across the "
    "same fixed prompts.\n\n"
    "Label conventions (read carefully before judging):\n"
    "- Text *outside* parentheses gives the core semantic pattern.\n"
    "- Text *inside* parentheses contains activation evidence, example "
    "tokens, negative controls ('not X'), and OCRS/causal annotations "
    "that describe which tokens the neuron *boosts in the output* when "
    "amplified. The OCRS tokens are the single best clue for THIS task: "
    "if the label says the neuron causally boosts certain words, those "
    "are exactly the words that should appear more often in the matching "
    "completion set.\n"
    "- A SECONDARY label may appear; if it is meta (e.g. 'Input-output "
    "divergence'), use it only as a tie-breaker.\n\n"
    "Pick the set whose completions show the strongest overlap with the "
    "label's core concept AND with its OCRS/causal tokens. Amplification "
    "is noisy — prioritise keyword and theme overlap over fluency.\n\n"
    "Line 1 of your reply: a brief justification (≤ 30 words).\n"
    "Line 2: ONLY the chosen set number — 1, 2, or 3 — with no other text."
)

JUDGE_USER_PROMPT = (
    "Description: {description}\n\n"
    "# Set 1\n{amps1}\n\n"
    "# Set 2\n{amps2}\n\n"
    "# Set 3\n{amps3}\n"
)


@dataclass
class OutputScore:
    """Score + payload for one (variant, feature) pair."""
    correct_choice: int
    chosen_index: int
    judge_reasoning: str
    real_set: str
    distractor_sets: List[str] = field(default_factory=list)
    kl_targets: List[float] = field(default_factory=list)

    def success(self) -> bool:
        return self.correct_choice == self.chosen_index

    def to_dict(self) -> dict:
        out = asdict(self)
        out["success"] = self.success()
        return out


def build_steered_set(
    local_model: Any, local_sae: Any, layer: int, feature: int,
    prompts: Sequence[str] = GEN_PROMPTS_DEFAULT,
    kl_targets: Sequence[float] = KL_DIV_VALUES_DEFAULT,
    n_new: int = 25,
) -> str:
    """Build one "amps" block: for each KL target, generate completions on
    each prompt and format them like the notebook's
    ``<+0.25>'prompt': 'completion'`` lines."""
    sections: List[str] = []
    for kl in kl_targets:
        completions = get_completions_for_kl_val(
            local_model, local_sae, list(prompts), layer, feature,
            target_kl=kl, neg=(kl < 0), n_new=n_new,
        )
        sign = "+" if kl >= 0 else ""
        header = f"<{sign}{kl}>"
        joined = f"\n{header}".join(
            f"'{prompts[i]}': '{completions[i]}'"
            for i in range(min(len(prompts), len(completions)))
        )
        sections.append(f"\n{header}{joined}")
    return "".join(sections)


def load_random_amps_pool(pool_path: Path) -> List[str]:
    """Load a list of pre-generated random-feature amps blocks."""
    if not pool_path.exists():
        raise FileNotFoundError(
            f"Random amps pool not found at {pool_path}. "
            f"Run scripts/prepare_random_amps.py first."
        )
    with open(pool_path, "rb") as fh:
        data = pickle.load(fh)
    if not isinstance(data, list) or not data:
        raise ValueError(f"Random amps pool at {pool_path} is empty or malformed")
    return [str(x) for x in data]


def _sample_distractors(pool: List[str], n: int, rng: random.Random) -> List[str]:
    if len(pool) < n:
        raise ValueError(
            f"Random amps pool has only {len(pool)} entries; need ≥ {n}. "
            f"Re-run scripts/prepare_random_amps.py with a larger --num."
        )
    return rng.sample(pool, n)


def _parse_choice(reply: str) -> Tuple[int, str]:
    """Pull the chosen set number from the judge's final non-empty line.
    Returns (chosen_index, reasoning). Defaults to 0 if no digit is found."""
    lines = [ln.strip() for ln in (reply or "").splitlines() if ln.strip()]
    if not lines:
        return 0, ""
    last = lines[-1]
    reasoning = "\n".join(lines[:-1]).strip() or last
    m = re.search(r"\d+", last)
    chosen = int(m.group()) if m else 0
    return chosen, reasoning


def compute_output_score(
    description: str, feature: int,
    llm_model: str,
    pool_path: Path,
    rng: Optional[random.Random] = None,
    backend: str = "local",
    local_model: Any = None, local_sae: Any = None, local_layer: int = 0,
    prompts: Sequence[str] = GEN_PROMPTS_DEFAULT,
    kl_targets: Sequence[float] = KL_DIV_VALUES_DEFAULT,
    n_new: int = 25,
    api_steer_kwargs: Optional[Dict[str, Any]] = None,
) -> OutputScore:
    """End-to-end Output Metric for one (feature, description) pair.

    Args:
        description: SAGE description text.
        feature: feature index. (Model/source are implicit in pool + local SAE.)
        llm_model: judge LLM passed to ``ask_agent``.
        pool_path: pickle of cached random-feature amps blocks.
        rng: optional RNG for reproducible distractor sampling + set ordering.
        backend: ``"local"`` (KL-tuned, matches notebook) or ``"api"``
            (fixed-strength fallback via ``api_steer_kwargs``).
    """
    rng = rng or random.Random()
    if backend == "local":
        if local_model is None or local_sae is None:
            raise ValueError("local backend needs local_model and local_sae")
        real_amps = build_steered_set(
            local_model, local_sae, local_layer, feature,
            prompts=prompts, kl_targets=kl_targets, n_new=n_new,
        )
    elif backend == "api":
        real_amps = _build_steered_set_api(
            feature, prompts, kl_targets, api_steer_kwargs or {},
        )
    else:
        raise ValueError(f"Unknown backend {backend!r}; expected 'local' or 'api'")

    pool = load_random_amps_pool(pool_path)
    distractors = _sample_distractors(pool, NUM_OF_DISTS, rng)

    all_sets = distractors + [real_amps]
    order = rng.sample(range(len(all_sets)), len(all_sets))
    ordered = [all_sets[i] for i in order]
    correct_choice = order.index(len(all_sets) - 1) + 1

    user = JUDGE_USER_PROMPT.format(
        description=description,
        amps1=ordered[0], amps2=ordered[1], amps3=ordered[2],
    )
    reply = ask_agent(llm_model, [
        {"role": "system", "content": JUDGE_SYS_PROMPT},
        {"role": "user", "content": user},
    ])
    chosen_index, reasoning = _parse_choice(reply)
    return OutputScore(
        correct_choice=correct_choice,
        chosen_index=chosen_index,
        judge_reasoning=reasoning,
        real_set=real_amps,
        distractor_sets=distractors,
        kl_targets=list(kl_targets),
    )


def _build_steered_set_api(
    feature: int, prompts: Sequence[str],
    kl_targets: Sequence[float], api_steer_kwargs: Dict[str, Any],
) -> str:
    """Fallback: use Neuronpedia /api/steer with a fixed strength per target.

    ``api_steer_kwargs`` must include at minimum ``model`` and ``source``;
    optional keys: ``strength_per_kl`` (mapping target_kl -> strength,
    default {+0.25:+4, +0.5:+8, -0.25:-4, -0.5:-8}), ``n_tokens``."""
    from tools.steering_api import steer_feature
    model = api_steer_kwargs["model"]
    source = api_steer_kwargs["source"]
    n_tokens = api_steer_kwargs.get("n_tokens", 25)
    strength_map = api_steer_kwargs.get("strength_per_kl") or {
        0.25: 4.0, 0.5: 8.0, -0.25: -4.0, -0.5: -8.0,
    }
    sections: List[str] = []
    for kl in kl_targets:
        strength = float(strength_map.get(kl, 4.0 * (1 if kl >= 0 else -1)))
        per_prompt: List[str] = []
        for prompt in prompts:
            result = steer_feature(
                model, source, feature, prompt=prompt,
                strength=strength, n_tokens=n_tokens,
            )
            per_prompt.append(
                f"'{prompt}': '{result.steered_text[:200].replace(chr(10), chr(92) + 'n')}'"
            )
        sign = "+" if kl >= 0 else ""
        header = f"<{sign}{kl}>"
        sections.append(f"\n{header}" + f"\n{header}".join(per_prompt))
    return "".join(sections)


